# Functions are assigned to variables via starred assignment
def func1():
    return 'ufpfh'


def func2():
    return (3, 52, 91)


def func3():
    return [4, 49, 73]


def func4():
    return {'zgfxk': 92, 'idink': 7, 'upcng': 5}

a, *b, c = func1, func2, func3, func4

d = a()
e = b[0]()
f = b[1]()
g = c()
