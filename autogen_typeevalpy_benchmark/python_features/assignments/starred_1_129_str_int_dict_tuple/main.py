# Functions are assigned to variables via starred assignment
def func1():
    return 'sxpvb'


def func2():
    return 50


def func3():
    return {'rbqxd': 97, 'pxzuw': 58, 'oahvh': 11}


def func4():
    return (97, 74, 79)

a, *b, c = func1, func2, func3, func4

d = a()
e = b[0]()
f = b[1]()
g = c()
