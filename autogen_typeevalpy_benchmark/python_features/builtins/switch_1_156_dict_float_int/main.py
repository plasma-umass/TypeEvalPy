#  A function is defined with switch statement in it.
def func(value):
    match value:
        case {'jccvh': 99, 'cdnns': 100, 'kjbqw': 85}:
            return 68.77
        case 68.77:
            return {'jccvh': 99, 'cdnns': 100, 'kjbqw': 85}
        case _:
            return "default"


a = func({'jccvh': 99, 'cdnns': 100, 'kjbqw': 85})
b = func(68.77)
c = func(15)
