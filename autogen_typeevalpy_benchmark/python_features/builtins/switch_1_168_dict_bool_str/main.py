#  A function is defined with switch statement in it.
def func(value):
    match value:
        case {'znikz': 21, 'jiolr': 88, 'azqfk': 96}:
            return False
        case False:
            return {'znikz': 21, 'jiolr': 88, 'azqfk': 96}
        case _:
            return "default"


a = func({'znikz': 21, 'jiolr': 88, 'azqfk': 96})
b = func(False)
c = func('xaipi')
