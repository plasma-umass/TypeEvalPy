#  A function is defined with switch statement in it.
def func(value):
    match value:
        case 'kiedj':
            return 57.21
        case 57.21:
            return 'kiedj'
        case _:
            return "default"


a = func('kiedj')
b = func(57.21)
c = func({'cqfin': 52, 'kuhfa': 62, 'orvxa': 31})
