#  A function is defined with switch statement in it.
def func(value):
    match value:
        case 'ormzr':
            return 78.11
        case 78.11:
            return 'ormzr'
        case _:
            return "default"


a = func('ormzr')
b = func(78.11)
c = func({'sbquj': 5, 'miifo': 47, 'toirs': 36})
