#  A function is defined with switch statement in it.
def func(value):
    match value:
        case 'ijqkz':
            return {'xdose': 63, 'acapu': 53, 'mqouk': 34}
        case {'xdose': 63, 'acapu': 53, 'mqouk': 34}:
            return 'ijqkz'
        case _:
            return "default"


a = func('ijqkz')
b = func({'xdose': 63, 'acapu': 53, 'mqouk': 34})
c = func(65)
