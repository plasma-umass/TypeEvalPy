#  A function is defined with switch statement in it.
def func(value):
    match value:
        case 11:
            return {'tghhj': 32, 'byqiy': 74, 'mbrku': 68}
        case {'tghhj': 32, 'byqiy': 74, 'mbrku': 68}:
            return 11
        case _:
            return "default"


a = func(11)
b = func({'tghhj': 32, 'byqiy': 74, 'mbrku': 68})
c = func('wclzj')
