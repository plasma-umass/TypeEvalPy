#  A function is defined with switch statement in it.
def func(value):
    match value:
        case (99, 73, 78):
            return {'jdyia': 71, 'ikqgk': 68, 'zjyuv': 94}
        case {'jdyia': 71, 'ikqgk': 68, 'zjyuv': 94}:
            return (99, 73, 78)
        case _:
            return "default"


a = func((99, 73, 78))
b = func({'jdyia': 71, 'ikqgk': 68, 'zjyuv': 94})
c = func('hnkzt')
