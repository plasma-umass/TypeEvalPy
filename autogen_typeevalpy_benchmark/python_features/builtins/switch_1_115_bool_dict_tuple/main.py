#  A function is defined with switch statement in it.
def func(value):
    match value:
        case True:
            return {'hbqxe': 89, 'gkhkl': 18, 'nycxh': 6}
        case {'hbqxe': 89, 'gkhkl': 18, 'nycxh': 6}:
            return True
        case _:
            return "default"


a = func(True)
b = func({'hbqxe': 89, 'gkhkl': 18, 'nycxh': 6})
c = func((40, 29, 84))
