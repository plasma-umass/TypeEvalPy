#  A function is defined with switch statement in it.
def func(value):
    match value:
        case {'bbbbk': 16, 'qdnwq': 62, 'mliko': 22}:
            return 'oimah'
        case 'oimah':
            return {'bbbbk': 16, 'qdnwq': 62, 'mliko': 22}
        case _:
            return "default"


a = func({'bbbbk': 16, 'qdnwq': 62, 'mliko': 22})
b = func('oimah')
c = func(80.54)
