#  A function is defined with switch statement in it.
def func(value):
    match value:
        case 11:
            return 'lllxa'
        case 'lllxa':
            return 11
        case _:
            return "default"


a = func(11)
b = func('lllxa')
c = func({'gqrvl': 27, 'vqbim': 10, 'rsuol': 17})
