#  A function is defined with switch statement in it.
def func(value):
    match value:
        case 'htatx':
            return False
        case False:
            return 'htatx'
        case _:
            return "default"


a = func('htatx')
b = func(False)
c = func({'lsnnz': 18, 'pwqxd': 87, 'zcytl': 86})
