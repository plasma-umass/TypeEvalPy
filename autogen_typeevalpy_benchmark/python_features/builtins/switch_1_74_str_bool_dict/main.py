#  A function is defined with switch statement in it.
def func(value):
    match value:
        case 'mufbb':
            return False
        case False:
            return 'mufbb'
        case _:
            return "default"


a = func('mufbb')
b = func(False)
c = func({'bxqih': 15, 'joyhc': 89, 'cmrkx': 11})
