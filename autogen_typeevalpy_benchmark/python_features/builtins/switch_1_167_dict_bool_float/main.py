#  A function is defined with switch statement in it.
def func(value):
    match value:
        case {'nvniv': 44, 'qkxxd': 12, 'psunw': 16}:
            return True
        case True:
            return {'nvniv': 44, 'qkxxd': 12, 'psunw': 16}
        case _:
            return "default"


a = func({'nvniv': 44, 'qkxxd': 12, 'psunw': 16})
b = func(True)
c = func(74.04)
