#  A function is defined with switch statement in it.
def func(value):
    match value:
        case {'xmmzg': 47, 'zoauw': 24, 'eubfn': 4}:
            return [1, 81, 20]
        case [1, 81, 20]:
            return {'xmmzg': 47, 'zoauw': 24, 'eubfn': 4}
        case _:
            return "default"


a = func({'xmmzg': 47, 'zoauw': 24, 'eubfn': 4})
b = func([1, 81, 20])
c = func('gcdbf')
