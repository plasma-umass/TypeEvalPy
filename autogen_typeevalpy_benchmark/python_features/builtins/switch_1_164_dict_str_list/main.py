#  A function is defined with switch statement in it.
def func(value):
    match value:
        case {'koymr': 8, 'zyfzd': 30, 'flxqk': 25}:
            return 'bhfht'
        case 'bhfht':
            return {'koymr': 8, 'zyfzd': 30, 'flxqk': 25}
        case _:
            return "default"


a = func({'koymr': 8, 'zyfzd': 30, 'flxqk': 25})
b = func('bhfht')
c = func([2, 47, 100])
