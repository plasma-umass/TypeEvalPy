#  A function is defined with switch statement in it.
def func(value):
    match value:
        case {'iiyhy': 23, 'eklir': 60, 'pupwp': 91}:
            return 6.05
        case 6.05:
            return {'iiyhy': 23, 'eklir': 60, 'pupwp': 91}
        case _:
            return "default"


a = func({'iiyhy': 23, 'eklir': 60, 'pupwp': 91})
b = func(6.05)
c = func('dbbqf')
