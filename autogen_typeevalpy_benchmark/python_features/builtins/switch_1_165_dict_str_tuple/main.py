#  A function is defined with switch statement in it.
def func(value):
    match value:
        case {'yomxl': 4, 'oonhp': 94, 'ijqad': 48}:
            return 'lvkcc'
        case 'lvkcc':
            return {'yomxl': 4, 'oonhp': 94, 'ijqad': 48}
        case _:
            return "default"


a = func({'yomxl': 4, 'oonhp': 94, 'ijqad': 48})
b = func('lvkcc')
c = func((95, 26, 4))
