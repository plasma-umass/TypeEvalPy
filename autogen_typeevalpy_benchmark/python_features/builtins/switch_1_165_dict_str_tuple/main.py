#  A function is defined with switch statement in it.
def func(value):
    match value:
        case {'daezd': 6, 'cgjxq': 35, 'vfqfk': 9}:
            return 'hyhbq'
        case 'hyhbq':
            return {'daezd': 6, 'cgjxq': 35, 'vfqfk': 9}
        case _:
            return "default"


a = func({'daezd': 6, 'cgjxq': 35, 'vfqfk': 9})
b = func('hyhbq')
c = func((61, 6, 70))
