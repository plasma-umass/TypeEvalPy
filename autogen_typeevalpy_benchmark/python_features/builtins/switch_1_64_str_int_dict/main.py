#  A function is defined with switch statement in it.
def func(value):
    match value:
        case 'qdheu':
            return 62
        case 62:
            return 'qdheu'
        case _:
            return "default"


a = func('qdheu')
b = func(62)
c = func({'ifajj': 51, 'vhpmm': 52, 'qtseb': 94})
