#  A function is defined with switch statement in it.
def func(value):
    match value:
        case {'nwqna': 80, 'umgfa': 68, 'gowjf': 44}:
            return 94
        case 94:
            return {'nwqna': 80, 'umgfa': 68, 'gowjf': 44}
        case _:
            return "default"


a = func({'nwqna': 80, 'umgfa': 68, 'gowjf': 44})
b = func(94)
c = func('kqjmu')
