#  A function is defined with switch statement in it.
def func(value):
    match value:
        case 25.55:
            return 'kxytt'
        case 'kxytt':
            return 25.55
        case _:
            return "default"


a = func(25.55)
b = func('kxytt')
c = func({'qnjbj': 52, 'xtjhz': 72, 'ixmkk': 95})
