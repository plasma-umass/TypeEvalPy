#  A function is defined with switch statement in it.
def func(value):
    match value:
        case {'fjpjz': 75, 'gueyf': 97, 'xvjke': 90}:
            return 'xgbhp'
        case 'xgbhp':
            return {'fjpjz': 75, 'gueyf': 97, 'xvjke': 90}
        case _:
            return "default"


a = func({'fjpjz': 75, 'gueyf': 97, 'xvjke': 90})
b = func('xgbhp')
c = func((42, 96, 89))
