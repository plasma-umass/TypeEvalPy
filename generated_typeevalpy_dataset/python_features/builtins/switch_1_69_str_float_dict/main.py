#  A function is defined with switch statement in it.
def func(value):
    match value:
        case 'wypqe':
            return 11.24
        case 11.24:
            return 'wypqe'
        case _:
            return "default"


a = func('wypqe')
b = func(11.24)
c = func({'mxhhq': 100, 'gmdme': 81, 'nrfyi': 31})
