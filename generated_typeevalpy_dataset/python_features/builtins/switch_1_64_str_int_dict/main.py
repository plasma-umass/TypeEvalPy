#  A function is defined with switch statement in it.
def func(value):
    match value:
        case 'akdqg':
            return 8
        case 8:
            return 'akdqg'
        case _:
            return "default"


a = func('akdqg')
b = func(8)
c = func({'inqtb': 62, 'yqpxj': 25, 'evbuo': 33})
