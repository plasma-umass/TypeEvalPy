#  A function is defined with switch statement in it.
def func(value):
    match value:
        case {'vrgfh': 11, 'dgvos': 68, 'cyzqg': 53}:
            return 58.55
        case 58.55:
            return {'vrgfh': 11, 'dgvos': 68, 'cyzqg': 53}
        case _:
            return "default"


a = func({'vrgfh': 11, 'dgvos': 68, 'cyzqg': 53})
b = func(58.55)
c = func(True)
