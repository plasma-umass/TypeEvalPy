#  A function is defined with switch statement in it.
def func(value):
    match value:
        case 81:
            return 'odebd'
        case 'odebd':
            return 81
        case _:
            return "default"


a = func(81)
b = func('odebd')
c = func({'yuqoy': 41, 'atyfb': 98, 'hnduf': 84})
