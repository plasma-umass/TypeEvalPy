#  A function is defined with switch statement in it.
def func(value):
    match value:
        case [6, 89, 97]:
            return 'pgxwj'
        case 'pgxwj':
            return [6, 89, 97]
        case _:
            return "default"


a = func([6, 89, 97])
b = func('pgxwj')
c = func({'nypwm': 47, 'jivqo': 2, 'qqehx': 100})
